  /// Get translation for a given text and target language
  /// Returns null if translation not found in database
  static String? getTranslation(String text, String targetLanguage) {
    final normalizedText = text.toLowerCase().trim();

    if (translations.containsKey(normalizedText)) {
      return translations[normalizedText]![targetLanguage];
    }

    return null;
  }

  /// Get all available phrases
  static List<String> getAllPhrases() {
    return translations.keys.toList();
  }

  /// Check if a phrase exists in the database
  static bool hasPhrase(String text) {
    return translations.containsKey(text.toLowerCase().trim());
  }

  /// Get all supported languages for a phrase
  static List<String> getSupportedLanguages(String text) {
    final normalizedText = text.toLowerCase().trim();
    if (translations.containsKey(normalizedText)) {
      return translations[normalizedText]!.keys.toList();
    }
    return [];
  }
